package com.biggestNumbersEx1;

public class AssignmentOperatorEx_01 
{

	public static void main(String[] args)
	{
	int num=100;
	//num=num+100
	num+=10;
	System.out.println(num);
	System.out.println("**********");
	
	int value=100;
	//value=value-100
	value-=50;
	System.out.println(value);
    System.out.println("*****************");
		
    int res=10;
    res+=10;
    res-=10;
    res*=10;
    System.out.println(res);
    System.out.println("*************");

    res/=10;
    res*=10;
	System.out.println(res);
	}

}
